def build_hunyuan_image_3_bucket_scheduler(policy: HunyuanImage3ResolutionPolicy) -> BucketScheduler:
    """Construct a :class:`BucketScheduler` from the HI3 resolution policy.

    Only ``synchronized_weighted`` is supported today; ``synchronized_curriculum``
    is reserved until its schema is frozen and raises ``NotImplementedError``.
    """
    config = policy.config
    if config.selection_mode == "synchronized_curriculum":
        raise NotImplementedError(
            "synchronized_curriculum bucket selection is not implemented yet; "
            "use selection_mode='synchronized_weighted'."
        )
    if config.selection_mode != "synchronized_weighted":
        raise ValueError(f"Unsupported bucket selection_mode: {config.selection_mode!r}.")
    return BucketScheduler(
        weights=policy.compute_bucket_weights(),
        scheduler_seed=int(config.scheduler_seed),
    )


def build_hunyuan_image_3_bucket_indexer(
    policy: HunyuanImage3ResolutionPolicy,
    *,
    default_base_size: int,
    width_key: str = "width",
    height_key: str = "height",
    image_key: str = "image",
) -> BucketIndexer:
    """Construct a :class:`BucketIndexer` bound to this policy's ``default_base_size``.

    Validates ``default_base_size`` up-front so a mistyped YAML entry fails
    loud at build time instead of misclassifying every sample.
    """
    if not isinstance(default_base_size, int) or isinstance(default_base_size, bool):
        raise TypeError("default_base_size must be an int.")
    if default_base_size <= 0:
        raise ValueError("default_base_size must be positive.")
    base_sizes = {bucket.base_size for bucket in policy.buckets}
    if default_base_size not in base_sizes:
        raise ValueError(
            f"default_base_size={default_base_size} is not present in the resolution policy "
            f"(active base sizes: {sorted(base_sizes)}). Adjust "
            f"``data.image_generation.default_base_size`` or ``resolution_policy.anchors``."
        )

    def _bucket_of(width: int, height: int) -> int:
        return int(policy.select_bucket(width=int(width), height=int(height), base_size=default_base_size).bucket_id)

    return BucketIndexer(
        bucket_of=_bucket_of,
        policy_fingerprint=policy.policy_hash(),
        width_key=width_key,
        height_key=height_key,
        image_key=image_key,
    )


def build_hunyuan_image_3_bucket_batch_sampler(
    policy: HunyuanImage3ResolutionPolicy,
    *,
    bucket_ids: "np.ndarray",
    dp_rank: int,
    dp_size: int,
    micro_batch_size: int,
    num_micro_batch: int,
    seed: int,
    schedule_fn: Callable[[int, int], int],
) -> BucketBatchSampler:
    """Construct a :class:`BucketBatchSampler` for this policy.

    The ``insufficient_bucket_policy`` is taken from the policy config so that
    the sampler and any downstream ``scheduler.restrict_to`` invocation stay in
    sync with the operator's declared expectation.
    """
    return BucketBatchSampler(
        bucket_ids=bucket_ids,
        all_bucket_ids=policy.all_bucket_ids,
        dp_rank=dp_rank,
        dp_size=dp_size,
        micro_batch_size=micro_batch_size,
        num_micro_batch=num_micro_batch,
        seed=seed,
        schedule_fn=schedule_fn,
        bucket_labels=policy.bucket_labels,
        insufficient_bucket_policy=policy.config.insufficient_bucket_policy,
    )


